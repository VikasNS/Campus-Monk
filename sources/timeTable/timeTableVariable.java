package timeTable;

public class timeTableVariable {
    String C1;
    String C2;
    String C3;

    public timeTableVariable(String c1, String c2, String c3) {
        this.C1 = c1;
        this.C2 = c2;
        this.C3 = c3;
    }

    public String getC1() {
        return this.C1;
    }

    public String getC2() {
        return this.C2;
    }

    public String getC3() {
        return this.C3;
    }
}
